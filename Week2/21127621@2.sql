CREATE TABLE CHUDE
(
	MACD nchar(4),
	TENCD nvarchar(30),

	PRIMARY KEY (MACD)
)

CREATE TABLE DETAI
(
	MADT char(3),
	TENDT nvarchar(50),
	CAPQL nchar(20),
	KINHPHI float,
	NGAYBD datetime,
	NGAYKT datetime,
	MACD nchar(4),
	GVCNDT char(3),

	PRIMARY KEY (MADT)
)

ALTER TABLE DETAI
ADD CONSTRAINT FK_DETAI_CHUDE
FOREIGN KEY (MACD)
REFERENCES CHUDE(MACD)

ALTER TABLE DETAI
ADD CONSTRAINT FK_DETAI_GIAOVIEN
FOREIGN KEY (GVCNDT)
REFERENCES GIAOVIEN(MAGV)


CREATE TABLE CONGVIEC
(
	MADT char(3),
	STT int,
	TENCV nvarchar(30),
	NGAYBD datetime,
	NGAYKT datetime,

	PRIMARY KEY (MADT, STT)
)

ALTER TABLE CONGVIEC
ADD CONSTRAINT FK_CONGVIEC_DETAI
FOREIGN KEY (MADT)
REFERENCES DETAI(MADT)


CREATE TABLE THAMGIADT
(
	MAGV char(3),
	MADT char(3),
	STT int,
	PHUCAP float,
	KETQUA nvarchar(3),

	PRIMARY KEY (MAGV, MADT, STT)
